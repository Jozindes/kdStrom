    public void vyplnZoznamPrilahlych() {
        ArrayList<IObjektovyTypNaMape> zoznam1 = this.upravovany.dajNehnutelnost().dajZoznamObjektov();
        DefaultTableModel model = (DefaultTableModel) zoznamPrilahlych.getModel();

        
        if (zoznam1 != null) {
            for (IObjektovyTypNaMape akt : zoznam1) {
                model.addRow(new Object[]{akt.dajSupisneCislo(), akt.dajPopis()});
            }
        }
    }

