    private void ulozitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ulozitActionPerformed
        int supisne = Integer.parseInt(this.supisneCislo.getText());
        String popisok = this.popis.getText();
        
        this.upravovany.dajNehnutelnost().nastavSupisneCislo(supisne);
        this.upravovany.dajNehnutelnost().nastavPopis(popisok);
        
        double surX = Double.parseDouble(this.suradnicaX.getText());
        double surY = Double.parseDouble(this.suradnicaY.getText());
        
        Kluc<Double> prvy = new Kluc(surX);
        Kluc<Double> druhy = new Kluc(surY);
        
        Porovnatelne[] poleKlucov = new Porovnatelne[2];
        poleKlucov[0] = prvy;
        poleKlucov[1] = druhy;
        
        char znak = this.sirka.getText().charAt(0);
        char znak1 = this.dlzka.getText().charAt(0);
        
        Nehnutelnost nn = new Nehnutelnost(supisne, popisok, znak, znak1);
         
        if ((this.upravovany.dajKlucNaIndexe(0).dajHodnotuKluca().compareTo(poleKlucov[0].dajHodnotuKluca()) != 0) ||
            (this.upravovany.dajKlucNaIndexe(1).dajHodnotuKluca().compareTo(poleKlucov[1].dajHodnotuKluca()) != 0)) {
            spravca.zmazNehnutelnost(poleKlucov, nn);
            
            IObjektovyTypNaMape data = upravovany.dajNehnutelnost();
            double[] novePoleKlucov = {surX, surY};
            spravca.pridajNovuNehnutelnost(novePoleKlucov, data);
        }
        
        JOptionPane.showMessageDialog(this, "Nehnutelnost uspesne upravena.");
        this.setVisible(false);
    }//GEN-LAST:event_ulozitActionPerformed

